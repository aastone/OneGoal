- (NSAttributedString *)numberAttributedStringWithNumberColor:(UIColor *)numberColor numberFont:(CGFloat)numberFont textColor:(UIColor *)textColor textFont:(CGFloat)textFont
{
    NSMutableAttributedString *originalAttrStr = [[NSMutableAttributedString alloc] initWithString:self];
    NSRange range = NSMakeRange(0, self.length);
    UIColor *defaultColor = [originalAttrStr attribute:NSForegroundColorAttributeName atIndex:0 effectiveRange:&range];
    UIFont *defaultFont = [originalAttrStr attribute:NSFontAttributeName atIndex:0 effectiveRange:&range];
    //传参可以为nil 或 0
    if (!textColor) {
        textColor = defaultColor;
    }
    
    if (!textFont) {
        textFont = defaultFont.pointSize;
    }
    
    if (!numberColor) {
        numberColor = defaultColor;
    }
    
    if (!numberFont) {
        numberFont = defaultFont.pointSize;
    }
    // 数字显示统一样式，其他文字显示统一样式
    originalAttrStr = [[NSMutableAttributedString alloc] initWithString:self attributes:@{NSFontAttributeName:[UIFont systemFontOfSize:textFont], NSForegroundColorAttributeName:textColor}];
    NSCharacterSet *numberSet = [NSCharacterSet characterSetWithCharactersInString:numberSets];//对数字和小数点都进行判断
    
    NSScanner *scanner = [NSScanner scannerWithString:self];
    NSNumber *prevScanLocation;
    while (![scanner isAtEnd]) {
        NSString *buffer;
        if ([scanner scanCharactersFromSet:numberSet intoString:&buffer]) {
            NSNumber *currentScanLocation = [NSNumber numberWithUnsignedInteger:scanner.scanLocation];//当scanner的结果为YES时，scanner.scanLocation会略过该值的位置再加1
            
            NSRange tmpRange = NSMakeRange(prevScanLocation.integerValue, currentScanLocation.integerValue-prevScanLocation.integerValue);
            if ([self rangeOfCharacterFromSet:numberSet options:NSCaseInsensitiveSearch range:NSMakeRange(prevScanLocation.integerValue, 1)].location != NSNotFound) {
                [originalAttrStr replaceCharactersInRange:tmpRange withAttributedString:[[NSMutableAttributedString alloc] initWithString:buffer attributes:@{NSFontAttributeName:[UIFont systemFontOfSize:numberFont], NSForegroundColorAttributeName:numberColor}]];
            }else {
                tmpRange = NSMakeRange(prevScanLocation.integerValue+1, currentScanLocation.integerValue-prevScanLocation.integerValue-1);
                [originalAttrStr replaceCharactersInRange:tmpRange withAttributedString:[[NSMutableAttributedString alloc] initWithString:buffer attributes:@{NSFontAttributeName:[UIFont systemFontOfSize:numberFont], NSForegroundColorAttributeName:numberColor}]];
            }
        }else {
            [scanner setScanLocation:([scanner scanLocation] +1)];
            prevScanLocation = [NSNumber numberWithUnsignedInteger:scanner.scanLocation];
        }
    }
    
    return originalAttrStr;
}

